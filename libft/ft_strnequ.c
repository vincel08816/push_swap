#include "libft.h"

/*
** checking if s1 == s2 in n
*/

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (s1 && s2 && ft_strncmp(s1, s2, n) == 0)
		return (1);
	return (0);
}
